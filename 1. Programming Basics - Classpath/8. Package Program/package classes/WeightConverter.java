package Converter;
public class WeightConverter {
    public static double kgtogram(double Kg){
        return Kg*1000;
    }
    public static double gramtoKg(double g){
        return g/1000;
    }
    public static double tonnetokg(double t){
        return t*1000;
    }
    public static double kgtotonne(double Kg){
        return Kg/1000;
    }
}
